package _07_Predicate;

public class Student {
	private String name;
	private String sex;
	private int com;
	
	public Student(String name, String sex, int com) {
		this.name = name;
		this.sex = sex;
		this.com = com;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public String getSex() {
		return sex;
	}

	public void setEng(int eng) {
		this.sex = sex;
	}

	public int getCom() {
		return com;
	}

	public void setCom(int com) {
		this.com = com;
	}
	
	
	

}
