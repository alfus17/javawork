package _02_conditional;

public class T01_ifelse {

	public static void main(String[] args) {
/*
 *  if(조건식) {
 *   	참일때
 *   }else{
 *   	거짓일때
 *   }
 * 
 * 
 */
		
		
		int num1 = 100;
		
		if(num1 >= 100 || num1 < 0) {
			System.out.println("양수");
		}else {
			System.out.println("양수가 아이다");
		}
		// 실행할 프로그램이 한줄이상이면 반드시 중괄호를 넣어야 된다
		
		
		
	}

}
